namespace PugLing.Model.Models.Constants;

/// <summary>
/// Provides short string constants (max 4 letters) for German verb tenses.
/// </summary>
public static class GermanTenses
{
    /// <summary>
    /// The present tense.
    /// </summary>
    public const string Präsens = "pres";

    /// <summary>
    /// The simple past tense.
    /// </summary>
    public const string Präteritum = "past";

    /// <summary>
    /// The present perfect tense.
    /// </summary>
    public const string Perfekt = "perf";

    /// <summary>
    /// The past perfect tense.
    /// </summary>
    public const string Plusquamperfekt = "plup";

    /// <summary>
    /// The future I tense.
    /// </summary>
    public const string FuturI = "fut1";

    /// <summary>
    /// The future II tense.
    /// </summary>
    public const string FuturII = "fut2";
}