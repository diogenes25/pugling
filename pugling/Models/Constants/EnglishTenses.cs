namespace pugling.Models.Constants
{
    /// <summary>
    /// Provides short string constants (max 4 letters) for English verb tenses.
    /// </summary>
    public static class EnglishTenses
    {
        /// <summary>
        /// The present simple tense.
        /// </summary>
        public const string PresentSimple = "pres";

        /// <summary>
        /// The present continuous tense.
        /// </summary>
        public const string PresentContinuous = "prgc"; // Present Progressive

        /// <summary>
        /// The past simple tense.
        /// </summary>
        public const string PastSimple = "past";

        /// <summary>
        /// The past continuous tense.
        /// </summary>
        public const string PastContinuous = "pagc"; // Past Progressive

        /// <summary>
        /// The present perfect tense.
        /// </summary>
        public const string PresentPerfect = "perf";

        /// <summary>
        /// The past perfect tense.
        /// </summary>
        public const string PastPerfect = "pape";

        /// <summary>
        /// The future simple tense.
        /// </summary>
        public const string FutureSimple = "futr";

        /// <summary>
        /// The future continuous tense.
        /// </summary>
        public const string FutureContinuous = "fucn";

        /// <summary>
        /// The future perfect tense.
        /// </summary>
        public const string FuturePerfect = "fupe";
    }
}