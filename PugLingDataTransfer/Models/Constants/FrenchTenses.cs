namespace PugLingDataTransfer.Models.Constants
{
    /// <summary>
    /// Provides short string constants (max 4 letters) for French verb tenses (ASCII field names).
    /// </summary>
    public static class FrenchTenses
    {
        /// <summary>
        /// The present tense (Présent).
        /// </summary>
        public const string Present = "pres";

        /// <summary>
        /// The imperfect tense (Imparfait).
        /// </summary>
        public const string Imparfait = "impa";

        /// <summary>
        /// The simple past tense (Passé simple).
        /// </summary>
        public const string PasseSimple = "pasi";

        /// <summary>
        /// The compound past tense (Passé composé).
        /// </summary>
        public const string PasseCompose = "paco";

        /// <summary>
        /// The future simple tense (Futur simple).
        /// </summary>
        public const string FuturSimple = "fusi";

        /// <summary>
        /// The future perfect tense (Futur antérieur).
        /// </summary>
        public const string FuturAnterieur = "fuan";
    }
}